def _run_user_code(task_id: str, code: str, workflow: Dict[str, Any]):
    """Execute user code with enhanced result handling."""
    rec = MANAGER.get(task_id)
    rec.state = "running"
    rec.progress = 0.02
    rec.message = "Starting execution"
    MANAGER.notify_threadsafe(
        task_id,
        {
            "event": "state",
            "state": rec.state,
            "progress": rec.progress,
            "message": rec.message,
        },
    )
    _log(task_id, "⏳ Starting execution")

    # Non-interactive matplotlib
    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import numpy as np  # noqa

    report = ReportBridge(task_id)

    glb = {
        "__name__": "__main__",
        "__package__": None,
        "torch": torch,
        "report": report,
    }
    loc: Dict[str, Any] = {}

    try:
        _log(task_id, "📜 Executing generated code...")
        rec.message = "Executing generated code"
        rec.progress = 0.12
        MANAGER.notify_threadsafe(
            task_id,
            {
                "event": "state",
                "state": rec.state,
                "progress": rec.progress,
                "message": rec.message,
            },
        )
        print(code)
        exec(code, glb, loc)  # user code
        _log(task_id, "✓ Code executed successfully")

        # If user code didn’t set results, try to infer something useful.
        if not rec.results:
            _log(task_id, "No explicit results set, attempting auto-detection...")
            trainer = loc.get("trainer")
            X_val, Y_val = loc.get("X_val"), loc.get("Y_val")
            time_series = loc.get("time_series", None)
            offset = loc.get("train_size", None)

            metrics = loc.get("metrics")
            if (
                metrics is None
                and trainer
                and (X_val is not None)
                and (Y_val is not None)
            ):
                _log(task_id, "🧮 Computing metrics via trainer.metrics(...)")
                rec.message = "Computing metrics"
                rec.progress = 0.35
                MANAGER.notify_threadsafe(
                    task_id,
                    {
                        "event": "state",
                        "state": rec.state,
                        "progress": rec.progress,
                        "message": rec.message,
                    },
                )
                try:
                    metrics = trainer.metrics(X_val, Y_val)
                except Exception as e:
                    _log(task_id, f"⚠️ metrics() failed: {e}")
                    metrics = None

            plot_b64 = None
            try:
                if trainer and (X_val is not None) and (Y_val is not None):
                    _log(task_id, "📈 Generating plot via trainer.plot_prediction(...)")
                    rec.message = "Generating plot"
                    rec.progress = 0.55
                    MANAGER.notify_threadsafe(
                        task_id,
                        {
                            "event": "state",
                            "state": rec.state,
                            "progress": rec.progress,
                            "message": rec.message,
                        },
                    )
                    fig = trainer.plot_prediction(
                        X_val,
                        Y_val,
                        full_series=time_series if time_series is not None else None,
                        offset=int(offset) if offset is not None else 0,
                        show=False,
                    )
                    buf = io.BytesIO()
                    fig.savefig(buf, format="png", bbox_inches="tight")
                    plt.close(fig)
                    buf.seek(0)
                    plot_b64 = base64.b64encode(buf.read()).decode("utf-8")
            except Exception as e:
                _log(task_id, f"⚠️ plot failed: {e}")

            out_id = pick_output_node_id(workflow) or "output_1"
            if metrics is not None:
                rec.results[out_id] = {"type": "metrics", "data": _json_safe(metrics)}
                MANAGER.notify_threadsafe(
                    task_id,
                    {
                        "event": "node_result",
                        "node_id": out_id,
                        "result": rec.results[out_id],
                    },
                )
            if plot_b64 is not None:
                plot_id = out_id if metrics is None else f"{out_id}_plot"
                rec.results[plot_id] = {"type": "plot", "data": plot_b64}
                MANAGER.notify_threadsafe(
                    task_id,
                    {
                        "event": "node_result",
                        "node_id": plot_id,
                        "result": rec.results[plot_id],
                    },
                )
            if not rec.results:
                rec.results[out_id] = {
                    "type": "text",
                    "data": "Execution finished (no results found).",
                }
                MANAGER.notify_threadsafe(
                    task_id,
                    {
                        "event": "node_result",
                        "node_id": out_id,
                        "result": rec.results[out_id],
                    },
                )

        rec.state = "success"
        rec.message = "Done"
        rec.progress = 1.0
        rec.finished_at = time.time()
        _log(task_id, f"✅ Finished with {len(rec.results)} result(s)")
        MANAGER.notify_threadsafe(
            task_id,
            {"event": "done", "success": True, "results": _json_safe(rec.results)},
        )
    except Exception as e:
        rec.state = "error"
        rec.message = str(e)
        rec.progress = 1.0
        rec.finished_at = time.time()
        tb = traceback.format_exc()
        _log(task_id, "❌ Error:\n" + tb)
        MANAGER.notify_threadsafe(
            task_id, {"event": "done", "success": False, "error": rec.message}
        )


