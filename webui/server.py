@app.post("/execute", response_model=ExecuteResponse)
def execute(req: ExecuteRequest):
    rec = MANAGER.create()
    MANAGER.submit(rec, _run_user_code, req.code, req.workflow)

    # Return immediately in async mode
    if not req.sync:
        return ExecuteResponse(success=True, task_id=rec.task_id)

    # For sync=True, wait briefly up to timeout
    deadline = time.time() + float(req.timeout_sec)
    while time.time() < deadline:
        if rec.state in ("success", "error", "cancelled"):
            break
        time.sleep(0.05)

    if rec.state == "success":
        return ExecuteResponse(success=True, task_id=rec.task_id, results=rec.results)
    if rec.state == "error":
        return ExecuteResponse(success=False, task_id=rec.task_id, error=rec.message)
    # still running
    return ExecuteResponse(success=True, task_id=rec.task_id)


@app.get("/status/{task_id}", response_model=StatusResponse)
def status(task_id: str):
    try:
        rec = MANAGER.get(task_id)
    except KeyError:
        raise HTTPException(status_code=404, detail="Task not found")
    # Return partial results if any, even while running
    return StatusResponse(
        task_id=task_id,
        state=rec.state,
        progress=rec.progress,
        message=rec.message,
        results=_json_safe(rec.results) if rec.results else None,
    )


