NODE_DEFS_PATH = os.path.join(os.path.dirname(__file__), "nodeDefinitions.js")


def load_static_nodes() -> dict:
    """Parse nodeDefinitions.js (object literal) into a Python dict."""
    if not os.path.exists(NODE_DEFS_PATH):
        return {}
    with open(NODE_DEFS_PATH, "r", encoding="utf-8") as f:
        content = f.read()

    match = re.search(r"const\s+NODE_TYPES\s*=\s*({[\s\S]*?});", content)
    if not match:
        print("[WARN] nodeDefinitions.js: No NODE_TYPES found.")
        return {}

    js_text = match.group(1)
    js_text = re.sub(r"//.*", "", js_text)
    js_text = re.sub(r"/\*.*?\*/", "", js_text, flags=re.S)
    js_text = re.sub(r"\s+", " ", js_text)
    js_text = re.sub(r"'", '"', js_text)
    js_text = re.sub(r"([{,]\s*)([a-zA-Z_][a-zA-Z0-9_-]*)\s*:", r'\1"\2":', js_text)
    js_text = re.sub(r",(\s*[}\]])", r"\1", js_text)

    try:
        return json.loads(js_text)
    except json.JSONDecodeError as e:
        debug_file = os.path.join(
            os.path.dirname(NODE_DEFS_PATH), "nodeDefinitions_debug.json"
        )
        with open(debug_file, "w", encoding="utf-8") as dbg:
            dbg.write(js_text)
        print(f"[ERROR] Could not parse nodeDefinitions.js: {e}")
        print(f"[DEBUG] Saved normalized version to {debug_file}")
        return {}


