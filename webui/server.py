class ReportBridge:
    """
    API exposed to user code via 'report'.
    Use:
      report.log("message")
      report.progress(0.5, "halfway")
      report.set_result(node_id, 'metrics', {...})
      report.artifact("plot.png", fig_or_bytes_or_path)
    """

    def __init__(self, task_id: str):
        self.task_id = task_id

    def log(self, text: str):
        _log(self.task_id, text)
        MANAGER.notify_threadsafe(self.task_id, {"event": "log", "line": text})

    def progress(self, value: float, message: str = ""):
        rec = MANAGER.get(self.task_id)
        rec.progress = max(0.0, min(1.0, float(value)))
        if message:
            rec.message = message
        _log(self.task_id, f"progress={rec.progress:.0%} {message}".strip())
        MANAGER.notify_threadsafe(
            self.task_id,
            {
                "event": "state",
                "state": rec.state,
                "progress": rec.progress,
                "message": rec.message,
            },
        )

    def set_result(self, node_id: str, result_type: str, data: Any):
        """
        result_type: 'plot' | 'image' | 'metrics' | 'table' | 'json' | 'array' | 'text'
        """
        import matplotlib.pyplot as plt

        rec = MANAGER.get(self.task_id)
        # Images / plots
        if result_type in ("plot", "image"):
            if hasattr(data, "savefig"):  # matplotlib figure
                buf = io.BytesIO()
                data.savefig(buf, format="png", bbox_inches="tight")
                plt.close(data)
                buf.seek(0)
                encoded = base64.b64encode(buf.read()).decode("utf-8")
                rec.results[node_id] = {"type": "plot", "data": encoded}
            elif isinstance(data, (bytes, bytearray)):
                encoded = base64.b64encode(bytes(data)).decode("utf-8")
                rec.results[node_id] = {"type": "image", "data": encoded}
            else:
                rec.results[node_id] = {"type": "text", "data": str(data)}
        elif result_type == "metrics":
            clean = {}
            if isinstance(data, dict):
                for k, v in data.items():
                    try:
                        if hasattr(v, "item"):
                            v = v.item()
                        if isinstance(v, (int, float)):
                            v = float(v)
                    except Exception:
                        pass
                    clean[str(k)] = v
                rec.results[node_id] = {"type": "metrics", "data": clean}
            else:
                rec.results[node_id] = {"type": "text", "data": str(data)}
        elif result_type == "table":
            if isinstance(data, dict) and "columns" in data and "rows" in data:
                rec.results[node_id] = {"type": "table", "data": data}
            else:
                rec.results[node_id] = {"type": "text", "data": str(data)}
        elif result_type == "json":
            try:
                json.dumps(data)
                rec.results[node_id] = {"type": "json", "data": data}
            except Exception:
                rec.results[node_id] = {"type": "text", "data": str(data)}
        elif result_type == "array":
            if hasattr(data, "tolist"):
                rec.results[node_id] = {"type": "array", "data": data.tolist()}
            elif isinstance(data, (list, tuple)):
                rec.results[node_id] = {"type": "array", "data": list(data)}
            else:
                rec.results[node_id] = {"type": "text", "data": str(data)}
        else:
            rec.results[node_id] = {"type": "text", "data": str(data)}

        _log(self.task_id, f"📊 Result set for node {node_id} (type: {result_type})")
        MANAGER.notify_threadsafe(
            self.task_id,
            {
                "event": "node_result",
                "node_id": node_id,
                "result": rec.results[node_id],
            },
        )

    def artifact(self, name: str, data: Any):
        """Save an artifact file under ./artifacts/<task>_<name>"""
        import shutil

        path = os.path.join(ARTIFACTS_DIR, f"{self.task_id}_{name}")
        if isinstance(data, (bytes, bytearray)):
            with open(path, "wb") as f:
                f.write(data)
        elif isinstance(data, str) and os.path.exists(data):
            shutil.copyfile(data, path)
        else:
            try:
                import matplotlib.pyplot as plt

                fig = data
                buf = io.BytesIO()
                fig.savefig(buf, format="png", bbox_inches="tight")
                with open(path, "wb") as f:
                    f.write(buf.getvalue())
                plt.close(fig)
            except Exception:
                with open(path, "w", encoding="utf-8") as f:
                    f.write(str(data))
        rec = MANAGER.get(self.task_id)
        rec.artifacts.append(os.path.basename(path))
        _log(self.task_id, f"📁 artifact saved: {os.path.basename(path)}")
        MANAGER.notify_threadsafe(
            self.task_id, {"event": "artifact", "name": os.path.basename(path)}
        )


