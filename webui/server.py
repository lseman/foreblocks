def pick_output_node_id(workflow: Dict[str, Any]) -> Optional[str]:
    try:
        for n in workflow.get("nodes", []):
            if str(n.get("type")) == "output":
                return n.get("id")
    except Exception:
        pass
    return None


def _log(task_id: str, msg: str):
    rec = MANAGER.get(task_id)
    stamp = time.strftime("%H:%M:%S")
    line = f"[{stamp}] ({task_id[:8]}) {msg}"
    rec.logs.append(line)
    print(line, flush=True)


