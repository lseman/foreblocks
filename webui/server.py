@app.websocket("/ws/{task_id}")
async def ws(task_id: str, websocket: WebSocket):
    try:
        await MANAGER.attach_ws(task_id, websocket)

        try:
            rec = MANAGER.get(task_id)
        except KeyError:
            await websocket.send_text(
                json.dumps({"event": "error", "message": "Task not found"})
            )
            await websocket.close()
            return

        # Initial snapshot
        await MANAGER.notify(
            task_id,
            {
                "event": "snapshot",
                "state": rec.state,
                "progress": rec.progress,
                "message": rec.message,
                "logs": rec.logs[-50:],
            },
        )

        last_len = len(rec.logs)

        # Passive ticker; pushes without needing client messages
        while True:
            await asyncio.sleep(0.2)

            # Stream new logs
            rec = MANAGER.get(task_id)
            if len(rec.logs) > last_len:
                await MANAGER.notify(
                    task_id, {"event": "logs", "lines": rec.logs[last_len:]}
                )
                last_len = len(rec.logs)

            # Heartbeat
            await MANAGER.notify(
                task_id,
                {
                    "event": "state",
                    "state": rec.state,
                    "progress": rec.progress,
                    "message": rec.message,
                },
            )

            if rec.state in ("success", "error", "cancelled"):
                await MANAGER.notify(
                    task_id,
                    {
                        "event": "done",
                        "success": rec.state == "success",
                        "results": rec.results if rec.state == "success" else None,
                        "error": rec.message if rec.state == "error" else None,
                    },
                )
                break

    except WebSocketDisconnect:
        await MANAGER.detach_ws(task_id, websocket)
    except Exception:
        await MANAGER.detach_ws(task_id, websocket)


@app.post("/cancel/{task_id}")
def cancel(task_id: str):
    try:
        rec = MANAGER.get(task_id)
    except KeyError:
        raise HTTPException(status_code=404, detail="Task not found")
    if rec.future and not rec.future.done():
        rec.state = "cancelled"
        rec.message = "Cancelled by user"
        rec.progress = 1.0
        rec.finished_at = time.time()
        _log(task_id, "🛑 Cancel requested")
        MANAGER.notify_threadsafe(
            task_id, {"event": "done", "success": False, "error": "Cancelled by user"}
        )
        return {"ok": True, "note": "Task flagged as cancelled"}
    return {"ok": False, "note": "Task already finished"}


