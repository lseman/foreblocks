# Task Manager
# ──────────────────────────────────────────────────────────────────────────────
class TaskManager:
    def __init__(self):
        self.pool = ThreadPoolExecutor(
            max_workers=MAX_WORKERS, thread_name_prefix="ts-worker"
        )
        self.tasks: Dict[str, TaskRecord] = {}
        self.ws_clients: Dict[str, List[WebSocket]] = {}
        self.loop: Optional[asyncio.AbstractEventLoop] = None  # set on startup

    def create(self) -> TaskRecord:
        tid = str(uuid.uuid4())
        rec = TaskRecord(task_id=tid)
        self.tasks[tid] = rec
        self.ws_clients[tid] = []
        return rec

    def get(self, task_id: str) -> TaskRecord:
        rec = self.tasks.get(task_id)
        if not rec:
            raise KeyError(f"Task {task_id} not found")
        return rec

    async def attach_ws(self, task_id: str, ws: WebSocket):
        await ws.accept()
        if task_id not in self.ws_clients:
            self.ws_clients[task_id] = []
        self.ws_clients[task_id].append(ws)

    async def detach_ws(self, task_id: str, ws: WebSocket):
        if task_id in self.ws_clients and ws in self.ws_clients[task_id]:
            self.ws_clients[task_id].remove(ws)

    async def notify(self, task_id: str, event: Dict[str, Any]):
        """Send to all connected clients for this task."""
        safe_event = _json_safe(event)
        for ws in list(self.ws_clients.get(task_id, [])):
            try:
                await ws.send_text(json.dumps(safe_event))
            except Exception:
                try:
                    await self.detach_ws(task_id, ws)
                except Exception:
                    pass

    def notify_threadsafe(self, task_id: str, event: Dict[str, Any]):
        """Schedule a notify from any thread."""
        if self.loop is None:
            return
        asyncio.run_coroutine_threadsafe(self.notify(task_id, event), self.loop)

    def submit(self, rec: TaskRecord, func, *args, **kwargs):
        future = self.pool.submit(func, rec.task_id, *args, **kwargs)
        rec.future = future
        return future


MANAGER = TaskManager()


